package com.performanceprediction.Dashboard.Profile;

/**
 *
 * @author sanji
 */
public class ProfileBean {
 private String teacherID;
 private String teachername;
 private String address;
 private String contact;
 private String email;
 private String assignedSubject;
 private String password;
 private String role;

    /**
     * @return the teacherID
     */
    public String getTeacherID() {
        return teacherID;
    }

    /**
     * @param teacherID the teacherID to set
     */
    public void setTeacherID(String teacherID) {
        this.teacherID = teacherID;
    }

   

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the contact
     */
    public String getContact() {
        return contact;
    }

    /**
     * @param contact the contact to set
     */
    public void setContact(String contact) {
        this.contact = contact;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the assignedSubject
     */
    public String getAssignedSubject() {
        return assignedSubject;
    }

    /**
     * @param assignedSubject the assignedSubject to set
     */
    public void setAssignedSubject(String assignedSubject) {
        this.assignedSubject = assignedSubject;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    /**
     * @return the teachername
     */
    public String getTeachername() {
        return teachername;
    }

    /**
     * @param teachername the teachername to set
     */
    public void setTeachername(String teachername) {
        this.teachername = teachername;
    }
 
 
}
